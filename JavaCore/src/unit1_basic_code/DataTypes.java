package unit1_basic_code;

public class DataTypes {
	
	/*
	 * 
	 * byte ( number, 1 byte )
	 * short ( number , 2 byte )
	 * long ( number , 4 byte )]
	 * float ( float number , 4 byte )
	 * double ( double number , 8 byte )
	 * char ( a char , 2 byte )
	 * boolean ( true or false, 1 byte )
	 * 
	 */

	public static void main(String[] args) {
		
		short my_short = 10;
		long my_long = 1000L;
		float my_float = (float) 4.45;
		double my_double = 45.66;
		boolean status = false;
		
		System.out.print(status);

	}

}
